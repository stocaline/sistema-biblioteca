    private Boolean validaCamposFuncionario() {
        boolean validador;
        if ((!txtNomeFuncionario.getText().isEmpty())
                && (!txtSenhaFuncionario.getText().isEmpty())
                && (!txtEnderecoFuncionario.getText().isEmpty())
                && (!txtEmailFuncionario.getText().isEmpty())
                && (!txtRegistroFuncionario.getText().isEmpty())
                && (!txtSalarioFuncionario.getText().isEmpty())
                && (!txtCpfFuncionario.getText().isEmpty())) {
            validador = true;
        } else {
            JOptionPane.showMessageDialog(null, "Todos os campo devem ser preenchidos");
            validador = false;
        }
        return validador;
    }

    private void cadastrarProfessor() {
        String nome, senha, endereco, email;
        int cpf, registroFuncionario, salario;

        nome = txtNomeFuncionario.getText();
        senha = txtSenhaFuncionario.getText();
        endereco = txtEnderecoFuncionario.getText();
        email = txtEmailFuncionario.getText();
        registroFuncionario = Integer.parseInt(txtRegistroFuncionario.getText());
        cpf = Integer.parseInt(txtCpfFuncionario.getText());
        salario = Integer.parseInt(txtSalarioFuncionario.getText());

        FuncionarioDTO objfuncionariodto = new FuncionarioDTO();
        objfuncionariodto.setNome(nome);
        objfuncionariodto.setSenha(senha);
        objfuncionariodto.setEndereco(endereco);
        objfuncionariodto.setEmail(email);
        objfuncionariodto.setCpf(cpf);
        objfuncionariodto.setSalario(salario);
        objfuncionariodto.setRegistro_funciona(registroFuncionario);

        FuncionarioDAO objfuncionariodao = new FuncionarioDAO();
        objfuncionariodao.cadastrarFuncionario(objfuncionariodto);
    }
