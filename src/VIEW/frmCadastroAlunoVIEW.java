    private Boolean validaCamposAluno() {
        boolean validador;
        if ((!txtNomeAluno.getText().isEmpty())
                && (!txtSenhaAluno.getText().isEmpty())
                && (!txtEnderecoAluno.getText().isEmpty())
                && (!txtEmailAluno.getText().isEmpty())
                && (!txtMatriculaAluno.getText().isEmpty())
                && (!txtTurmaAluno.getText().isEmpty())
                && (!txtCpfAluno.getText().isEmpty())) {
            validador = true;
        } else {
            JOptionPane.showMessageDialog(null, "Todos os campo devem ser preenchidos");
            validador = false;
        }
        return validador;
    }

    private void cadastrarAluno() {
        String nome, senha, endereco, email, tipo,turma;
        int cpf, matriculaAluno;

        nome = txtNomeAluno.getText();
        senha = txtSenhaAluno.getText();
        endereco = txtEnderecoAluno.getText();
        email = txtEmailAluno.getText();
        matriculaAluno = Integer.parseInt(txtMatriculaAluno.getText());
        cpf = Integer.parseInt(txtCpfAluno.getText());
        turma = txtTurmaAluno.getText();
        tipo = "Aluno";

        AlunoDTO objalunodto = new AlunoDTO();
        objalunodto.setNome(nome);
        objalunodto.setSenha(senha);
        objalunodto.setEndereco(endereco);
        objalunodto.setEmail(email);
        objalunodto.setCpf(cpf);
        objalunodto.setTurma(turma);
        objalunodto.setMatricula_aluno(matriculaAluno);
        objalunodto.setTipo(tipo);
        
        AlunoDAO objalunodao = new AlunoDAO();
        objalunodao.cadastrarAluno(objalunodto);
    }
